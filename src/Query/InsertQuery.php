<?php

namespace Flatbase\Query;

class InsertQuery extends Query
{
    protected $values;

    /**
     * @return mixed
     */
    public function getValues()
    {
        return $this->values;
    }

    /**
     * @param mixed $values
     */
    public function setValues($values)
    {
        $this->values = $values;
    }
}